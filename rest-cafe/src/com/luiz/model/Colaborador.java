package com.luiz.model;
//Entidade Colaborador do banco representada no model com seus getters e setters
public class Colaborador {

	
	private int cpf_colab;
	private String nome_colab;
	
	
	
	public int getCpf_colab() {
		return cpf_colab;
	}
	public void setCpf_colab(int cpf_colab) {
		this.cpf_colab = cpf_colab;
	}
	public String getNome_colab() {
		return nome_colab;
	}
	public void setNome_colab(String nome_colab) {
		this.nome_colab = nome_colab;
	}
	
	
	
}

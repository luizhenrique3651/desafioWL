package com.luiz.model;

public class ItemCafe {
	
	private int id_item;
	private int cpf_colaborador;
	private  String nome_item;
	
	
	
	public int getId_item() {
		return id_item;
	}
	public void setId_item(int id_item) {
		this.id_item = id_item;
	}
	public int getCpf_colaborador() {
		return cpf_colaborador;
	}
	public void setCpf_colaborador(int cpf_colaborador) {
		this.cpf_colaborador = cpf_colaborador;
	}
	public String getNome_item() {
		return nome_item;
	}
	public void setNome_item(String nome_item) {
		this.nome_item = nome_item;
	}
	
	
	
	

}
